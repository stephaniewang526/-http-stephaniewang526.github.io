        <div class="col-lg-12">
            <hr>
            <h3>Our Mission</h3>
            <p>
                To promote knowledge, and original clinical and basic research, in every aspect of androgen excess disorders in order to
                improve the understanding, diagnosis, treatment, and prevention of these disorders.
            </p>
            <hr>
            <h3>Our Vision</h3>
            <p>
                The Society will strive to serve as the premier international multidisciplinary organization fostering and encouraging quality and innovation in
                clinical and basic research related to the etiology, diagnosis, treatment and prevention of androgen excess disorders.
                <ul>
                    <li>Promote excellence in basic and clinical research in androgen excess that improves the detection and treatment of these disorders.</li>
                    <li>Serve as a forum for the interchange of ideas between professionals from different scientific and clinical backgrounds, striving to stimulate evidence-based studies that guide the management of these disorders.</li>
                    <li>Educate research-sponsoring agencies in the U.S. and elsewhere, either alone or in partnership with patient groups, regarding the importance, priorities and need for funding of studies of androgen excess disorders.</li>
                    <li>Provide a site for the publication of announcements of investigators in search of collaborators, either for additional expertise or to aid in subject recruitment.</li>
                    <li>Produce and disseminate information (e.g. on the internet) regarding androgen excess disorders for patients and the lay public in general.</li>
                    <li>Provide a forum for the collection and dissemination of recent findings in androgen excess research, either published or presented at meetings, which are updated regularly and made available in internet-based form.</li>
                    <li>Develop universal diagnostic criteria for the screening, diagnosis, and treatment of androgen excess disorders.</li>
                    <li>Provide a forum for the direct interchange of ideas through the sponsorship or co-sponsorship of periodic scientific meetings.</li>
                    <li>Seek funds to provide seed monies for small starter projects in androgen excess research.</li>
                </ul>

            </p>
            <hr>
            <h3 id="history">Our History</h3>
            <p>
                The field of androgen excess research, particularly the study of polycystic ovary syndrome and its relationship to metabolic abnormalities, grew exponentially during the 1990s. This was associated with an increased awareness of the disorder among patients and the general public, the increasing degree of collaboration among investigators from different disciplines, and pharmaceutical interests which drove specific research in this area. As it became evident that the number of patients with androgen excess represented a significant fraction of the population, and as the number of investigators in this area was growing rapidly, the need for an androgen excess investigator-initiated society became increasingly evident.
            </p>
