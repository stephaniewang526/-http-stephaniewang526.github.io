            <div class="col-md-6">
                <h2>Androgen Excess and PCOS Society</h2>
                <p>The Androgen Excess and PCOS Society is an international organization dedicated to promoting the generation and dessemination knowledge related to all aspects of androgen excess disorders, including:</p>
                <ul>
                    <li>Polycystic Ovary Syndrome</li>
                    <li>Adrenal Hyperplasia</li>
                    <li>Idiopathic Hirsutism</li>
                    <li>Premature Adrenarche</li>
                </ul>
                <p>Members include basic and clinical scientists, and clinicians, whose major interest is the etiology, diagnosis, treatment and prevention of androgen excess disorders. The Society disseminates information to the medical and scientific community, and the general public.</p>
            </div>
